/**
 * @class UIShaker
 * @extends UIElement
 * Shaker. Causes all parent elements (typically scenes) to shake based on set parameters when enabled (setEnable(true)). Will automatically be disabled after the shaking finishes.
 *
 */

/**
 * @method setAmplitudeX
 * Sets amplitude on the horizontal axis.
 * @param {Number} value When the value is positive, it will move right first. When it is negative, it will move left first.
 * @return {UIElement} Returns element.
 */

/**
 * @method setAmplitudeY
 * Sets amplitude on the vertical axis.
 * @param {Number} value When the value is positive, it will move down first. When it is negative, it will move up first.
 * @return {UIElement} Returns element.
 */

/**
 * @method setDuration
 * Sets the duration for the shaking to continue.
 * @param {Number} value The duration for the shaking to continue.
 * @return {UIElement} Returns element.
 */

/**
 * @method setTimes
 * Sets the number of shakes.
 * @param {Number} value Number.
 * @return {UIElement} Returns element.
 */

