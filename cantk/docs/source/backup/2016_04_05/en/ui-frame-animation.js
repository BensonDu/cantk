/**
 * @class UIFrameAnimation
 * @extends UIImage
 * Frame animation. Creates an animation effect by playing multiple images.  Images can be split into groups, whose names are determined when played.
 *
 */

/**
 * @method resume 
 * Unpause animation.
 * @return {UIElement} Returns element.
 *
 */

/**
 * @method pause
 * Pause animation.
 * @return {UIElement} Returns element.
 *
 */

/**
 * @method stop 
 * Stop animation.
 * @return {UIElement} Returns element.
 *
 */

/**
 * @method play
 * Play animation.
 * @param {String} name Group name.
 * @param {Number} repeatTimes Times played. 
 * @param {Function} onDone (optional) Callback function for after animation finishes a set number of times.
 * @param {Function} onOneCycle (optional) Callback function after animation finishes once.
 *
 */

