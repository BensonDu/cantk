/**
 * @class UILabel
 * @extends UIElement
 * Used to display text strings.
 *
 */

/**
 * @event onChanged
 * This event fires when the document changes.
 * @param {String} value Current document.
 */

/**
 * @method setTextAlign
 * Sets the text's horizontal alignment methods for the element.
 * @param {String} textAlign Horizontal alignment method. You can choose "left", "center", or "right".
 * @return {UIElement} Returns element.
 */

/**
 * @method setTextBaseline
 * Sets the text's vertical alignment methods for the element.
 * @param {String} textBaseline Vertical alignment method. You can choose "top", "middle", "bottom".
 * @return {UIElement} Returns element.
 */

/**
 * @method getTextAlign
 * Gets the text's horizontal alignment methods for the element.
 * @return {String} Returns the horizontal alignment method.
 */

/**
 * @method getTextBaseline
 * Gets the text's vertical alignment methods for the element.
 * @return {String} Returns the vertical alignment method.
 */

